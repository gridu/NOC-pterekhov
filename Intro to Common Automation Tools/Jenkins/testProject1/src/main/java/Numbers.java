public class Numbers {
    public double SUM (double N, double M){
        return N + M;
    }

    public double DIFF(double N, double M){
        return N - M;
    }

    public double MULTIPLY(double N, double M){
        return N * M;
    }

    public double DIVIDE(double N, double M){
        return  N / M;
    }
}
